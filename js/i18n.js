const LANG_KEY = "a2z-lang";

function currentLang() {
  return localStorage.getItem(LANG_KEY) === "ar" ? "ar" : "en";
}

function applyLang(lang) {
  const dict = I18N[lang] || I18N.en;
  const root = document.documentElement;

  root.lang = lang;
  root.dir = lang === "ar" ? "rtl" : "ltr";
  localStorage.setItem(LANG_KEY, lang);

  // page <title>
  const page = document.body.dataset.page;
  if (page && dict["title_" + page]) document.title = dict["title_" + page];

  // text / markup
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const val = dict[el.dataset.i18n];
    if (val !== undefined) el.innerHTML = val;
  });

  // placeholders
  document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
    const val = dict[el.dataset.i18nPh];
    if (val !== undefined) el.placeholder = val;
  });

  // footer year (the rights line is re-rendered above)
  const yearEl = document.getElementById("year");
  if (yearEl) yearEl.textContent = new Date().getFullYear();

  // toggle button shows the OTHER language
  const toggle = document.getElementById("lang-toggle");
  if (toggle) {
    toggle.querySelector(".lang-label").textContent = lang === "ar" ? "English" : "العربية";
    toggle.setAttribute("aria-label", lang === "ar" ? "Switch to English" : "التبديل إلى العربية");
  }
}

document.addEventListener("DOMContentLoaded", () => {
  applyLang(currentLang());
  const toggle = document.getElementById("lang-toggle");
  if (toggle) {
    toggle.addEventListener("click", () => {
      applyLang(currentLang() === "ar" ? "en" : "ar");
    });
  }
});
